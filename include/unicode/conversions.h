class str_t {
  std::array<char_t, 3> inner = {'\0', '\0', '\0'};

 public:
  constexpr str_t(std::initializer_list<char_t> list) {
    std::ranges::copy(list, inner.begin());
  }

  [[nodiscard]] constexpr auto size() const noexcept -> size_t {
    return std::distance(inner.begin(), std::ranges::find(inner, '\0'));
  }

  [[nodiscard]] constexpr auto begin() const noexcept {
    return inner.begin();
  }

  [[nodiscard]] constexpr auto end() const noexcept {
    return inner.begin() + size();
  }
};

constexpr auto bsearch_table(char_t ch, std::span<const std::pair<char_t, str_t>> table) noexcept
    -> std::optional<str_t> {
  auto first = [](auto&& pair) {
    return pair.first;
  };
  if (auto found = std::ranges::lower_bound(table, ch, std::less{}, first);
      found != table.end() && ch >= first(*found)) {
    return {found->second};
  } else {
    return std::nullopt;
  }
}

